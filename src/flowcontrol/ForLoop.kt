package flowcontrol

fun main(args: Array<String>) {
    //iterate and print number from 1 to 100
    val range = 1..100
    for (n in range) {
        println(n)
    }
    //also can iterate over string
    val name = "Khalid"
    for (c in name) {
        println(c)
    }
    //iterate for array
    val array = arrayOf(1, 2, 3, 4, 5)
    for (a in array) {
        println(a)
    }
}